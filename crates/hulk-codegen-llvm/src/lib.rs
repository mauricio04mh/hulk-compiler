    #[test]
    fn emits_minimal_print_program() {
        let program = IrProgram {
            types: vec![],
            data: vec![],
            functions: vec![IrFunction {
                id: FunctionId(0),
                name: "entry".to_string(),
                kind: IrFunctionKind::Entry,
                params: vec![],
                locals: vec![],
                temps: vec![
                    IrTemp {
                        id: TempId(0),
                        ty: IrTypeRef::Number,
                    },
                    IrTemp {
                        id: TempId(1),
                        ty: IrTypeRef::Object,
                    },
                ],
                return_type: IrTypeRef::Object,
                body: vec![
                    IrInstr::Binary {
                        dst: IrPlace::Temp(TempId(0)),
                        op: IrBinaryOp::Add,
                        left: IrValue::ConstNumber(2.0),
                        right: IrValue::ConstNumber(3.0),
                    },
                    IrInstr::Call {
                        dst: Some(IrPlace::Temp(TempId(1))),
                        function: "print".to_string(),
                        args: vec![IrValue::Temp(TempId(0))],
                    },
                    IrInstr::Return(Some(IrValue::Temp(TempId(1)))),
                ],
            }],
            entry: FunctionId(0),
        };

        let llvm = emit_llvm(&program).expect("codegen should pass");
        assert!(llvm.contains("declare void @hulk_print_number(double)"));
        assert!(llvm.contains("define ptr @hulk_entry()"));
        assert!(llvm.contains("fadd double 2.0, 3.0"));
        assert!(llvm.contains("call void @hulk_print_number(double"));
        assert!(llvm.contains("define i32 @main()"));
    }

